package lessons;

public class TwoDimensionalArrays {

	public static void main(String[] args) {
		
		double[][] chocolateBars = new double[4][3];

		chocolateBars[0][0] = 2.00;
		chocolateBars[0][1] = 10;
		chocolateBars[1][0] = 1.50;
		chocolateBars[1][1] = 13;
		chocolateBars[2][0] = 1.00;
		chocolateBars[2][1] = 11;
		chocolateBars[3][0] = 1.25;
		chocolateBars[3][1] = 12;
		
	}

}
